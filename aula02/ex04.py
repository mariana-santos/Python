# 4. Calcular e exibir a média aritmética de quatro valores quaisquer que serão digitados.

n1 = float(input("Selecione o primeira valor: "))
n2 = float(input("Selecione o segunda valor: "))
n3 = float(input("Selecione o terceira valor: "))
n4 = float(input("Selecione o quarta valor: "))

media = (n1 + n2 + n3 + n4)/4

print(f"Média das valores: {media}")